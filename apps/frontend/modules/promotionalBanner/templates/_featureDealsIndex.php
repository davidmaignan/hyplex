<script type="text/javascript">

    $('document').ready(function(){
        $('a.featureDeals-link').click(function(){
            var value = $(this).html()-1;
            var top = -180*value+'px';
            $('#featureDeals-container').animate({top:top});
            $('a.featureDeals-link').removeClass('selected');
            $(this).addClass('selected');
            
            $('article.featureDeals div.desc').hide();
            switch(true){
                
                case $(this).html() == 1:
                $('#featureDeals-1').children('div.desc').show();
                break;
                
                case $(this).html() == 2:
                $('#featureDeals-6').children('div.desc').show();
                break;
                
                case $(this).html() == 3:
                $('#featureDeals-11').children('div.desc').show();
                break;
                
                case $(this).html() == 4:
                $('#featureDeals-16').children('div.desc').show();
                break;
                
                case $(this).html() == 5:
                $('#featureDeals-21').children('div.desc').show();
                break;
                    
                
            }
            
            
            
            return false;
        });
        
        
        $('article.featureDeals').click(function(){
            $('article.featureDeals div.desc').hide();
            $(this).children('div.desc').show();
        });
        
    });

</script>